

# ================
# NUMBER
# ================


### Integer (정수형)
# a = 123
# b = -178
# c = 0
# print(a, b, c)    # 123 -178 0


### Floating-point (실수형)
# a = 1.2
# b = -3.45
# print(a, b)     # 1.2 -3.45

# a = 4.24E10
# b = 4.2e-10
# print(a, b)     # 42400000000.0 4.2e-10


### Octal (8진수)
# a = 0o177
# b = 0O177
# print(a, b)    # 127 127


### Hexadecimal (16진수)
# a = 0x8ff
# b = 0xabc
# print(a, b)     # 2303 2748



# ================
# OPERATOR
# ================

### +, -, *, / (사칙연산)
# a = 3
# b = 4
# print(a + b)    # 7
# print(a * b)    # 12
# print(a / b)    # 0.75


### ** (제곱근)
# a = 3
# b = 4
# print (a ** b)    # 81


### % (나눗셈의 나머지)
# print(7 % 3)    # 1


### // (나눗셈의 몫)
print(7 / 4)    # 1.75
print(7 // 4)   # 1


